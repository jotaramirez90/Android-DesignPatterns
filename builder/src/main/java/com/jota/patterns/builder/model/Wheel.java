package com.jota.patterns.builder.model;

public class Wheel {

  private int diameter;
  private String tire;

  public Wheel() {
  }

  public int getDiameter() {
    return diameter;
  }

  public void setDiameter(int diameter) {
    this.diameter = diameter;
  }

  public String getTire() {
    return tire;
  }

  public void setTire(String tire) {
    this.tire = tire;
  }
}
